   <h2>इतिहास एवं पुरातत्व शोध संस्थान संग्रहालय बालाघाट </h2><br>
   
    <p> इतिहास एवं पुरातत्व शोध संस्थान संग्रहालय बालाघाट ( मध्य प्रदेश )
                   आज हम आपको मध्य प्रदेश के बालाघाट जिले में स्थित पुरातत्व शोध संस्थान संग्रहालय के इतिहास के बारे में इस आर्टिकल में विस्तार से बताएंगे तो इस आर्टिकल के अंत तक बने रहिए ।
इतिहास एवं पुरातत्व सोच संधान संग्रहालय बालाघाट मध्य प्रदेश की स्थापना 14 सितंबर 1984 को आचार्य डॉ वीरेंद्र सिंह गहरवार वीर ने किया था।

शासन और प्रशासन के सहयोग से अस्थाई जिला पुरातत्व संग्रहालय 26 जनवरी 1988 को बनाया गया जहां सूक्ष्मा पुरातत्व महत्व की प्रतिमाओं को आचार्य डॉ वीरेंद्र सिंह गहरवार वीर ने संग्रहित किया।

पुनः स्थाई इतिहास एवं पुरातत्व शोध संस्थान संग्रहालय 1991 - 92 में बनाया गया और जिले की धरोहर को स्थायित्व प्रदान किया। मानव संसाधन विकास मंत्रालय भारत सरकार नई दिल्ली तथा अन्य संसाधनों से 1999 में संग्रहालय में मूलभूत सुविधाओं से सर्वोच्च प्राथमिक आधार पर नए स्वरूप में उजागर किया। "श्री भरत यादव " कलेक्टर के मार्गदर्शन और जन सहयोग से 26 जनवरी 2017 को नैरोगेज बोगी को संग्रहालय में स्थापित किया गया।
</p>



